import gsap from 'gsap';
import * as THREE from 'three';

export function transitionColor (mesh, toColor, duration) {
    const clock = new THREE.Clock()
    let startTime = clock.getElapsedTime()
    const startColor = new THREE.Color( mesh.material.color )
    const endColor = new THREE.Color( toColor )

    const onUpdate = (startTime) => {
        const elapsedTime = clock.getElapsedTime()
        const deltaTime = elapsedTime - startTime
        const alpha = deltaTime / duration

        // mesh.material.color.copy( startColor ).lerpHSL( endColor, alpha )
        mesh.material.color.lerpColors( startColor, endColor, alpha )
    }

    const onComplete = () => {
        mesh.material.color.set( endColor )
    }

    gsap.to({}, { onUpdate, onUpdateParams: [startTime], onComplete, duration })
}

